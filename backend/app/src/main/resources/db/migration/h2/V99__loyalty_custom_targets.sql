EXECUTE IMMEDIATE (
    SELECT 'ALTER TABLE loyalty_programs DROP CONSTRAINT "' || constraint_name || '"'
    FROM information_schema.check_constraints
    WHERE constraint_schema = 'public'
      AND check_clause LIKE '%nth_value%'
    LIMIT 1
);

ALTER TABLE loyalty_programs
    ADD CONSTRAINT IF NOT EXISTS loyalty_programs_nth_value_range_check
    CHECK (nth_value BETWEEN 2 AND 50);

EXECUTE IMMEDIATE (
