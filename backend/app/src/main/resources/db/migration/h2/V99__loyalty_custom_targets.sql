    FROM information_schema.check_constraints
    WHERE constraint_schema = 'public'
      AND check_clause LIKE '%target_type%'
      AND check_clause LIKE '%CATEGORY_TYPE%'
    LIMIT 1
);

EXECUTE IMMEDIATE (
    SELECT 'ALTER TABLE loyalty_program_earn_targets DROP CONSTRAINT "' || constraint_name || '"'
    FROM information_schema.check_constraints
    WHERE constraint_schema = 'public'
      AND check_clause LIKE '%semantic_type%'
      AND check_clause LIKE '%HOOKAH%'
    LIMIT 1
);

DROP INDEX IF EXISTS uq_loyalty_program_earn_targets_program;

ALTER TABLE loyalty_program_earn_targets
    ALTER COLUMN semantic_type DROP NOT NULL;

ALTER TABLE loyalty_program_earn_targets
    ADD COLUMN IF NOT EXISTS menu_item_id BIGINT NULL REFERENCES menu_items(id) ON DELETE CASCADE;

ALTER TABLE loyalty_program_earn_targets
    ADD CONSTRAINT IF NOT EXISTS loyalty_program_earn_targets_target_type_check
    CHECK (target_type IN ('CATEGORY_TYPE', 'MENU_ITEM'));

ALTER TABLE loyalty_program_earn_targets
    ADD CONSTRAINT IF NOT EXISTS loyalty_program_earn_targets_semantic_type_check
    CHECK (semantic_type IS NULL OR semantic_type IN ('HOOKAH'));

ALTER TABLE loyalty_program_earn_targets
    ADD CONSTRAINT IF NOT EXISTS loyalty_program_earn_targets_shape_check
    CHECK (
        (target_type = 'CATEGORY_TYPE' AND semantic_type = 'HOOKAH' AND menu_item_id IS NULL)
        OR
        (target_type = 'MENU_ITEM' AND semantic_type IS NULL AND menu_item_id IS NOT NULL)
    );

CREATE INDEX IF NOT EXISTS idx_loyalty_program_earn_targets_program
    ON loyalty_program_earn_targets (program_id, target_type, semantic_type, menu_item_id);

CREATE TABLE IF NOT EXISTS loyalty_program_reward_targets (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    program_id BIGINT NOT NULL REFERENCES loyalty_programs(id) ON DELETE CASCADE,
    target_type VARCHAR(32) NOT NULL CHECK (target_type IN ('CATEGORY_TYPE', 'MENU_ITEM')),
    semantic_type VARCHAR(32) NULL CHECK (semantic_type IS NULL OR semantic_type IN ('HOOKAH')),
    menu_item_id BIGINT NULL REFERENCES menu_items(id) ON DELETE CASCADE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CHECK (
        (target_type = 'CATEGORY_TYPE' AND semantic_type = 'HOOKAH' AND menu_item_id IS NULL)
        OR
        (target_type = 'MENU_ITEM' AND semantic_type IS NULL AND menu_item_id IS NOT NULL)
    )
);

CREATE INDEX IF NOT EXISTS idx_loyalty_program_reward_targets_program
    ON loyalty_program_reward_targets (program_id, target_type, semantic_type, menu_item_id);

INSERT INTO loyalty_program_reward_targets (program_id, target_type, semantic_type)
SELECT lp.id, 'CATEGORY_TYPE', 'HOOKAH'
FROM loyalty_programs lp
WHERE NOT EXISTS (
    SELECT 1
    FROM loyalty_program_reward_targets lprt
    WHERE lprt.program_id = lp.id
);

